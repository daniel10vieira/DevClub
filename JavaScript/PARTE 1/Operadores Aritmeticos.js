/* 
    OPERADORES ARITMÉTICOS
        - + Adição          20 + 30 
        - - Subtração       20 - 30
        - * Multiplicação   20 * 30 
        - / Divisão         20 / 30
        - % Resto           20 % 3
        - ++ Incremento     myNumber++  (A cda vez que coloca ++ na frente da variável, é adicionado +1)
        - -- Decremento     myNumber--  (A cda vez que coloca -- na frente da variável, é subtraído -1)
        - ** Exponencial    console.log(2 ** 2)
*/

let myNumberOne = 10
myNumberOne++
myNumberOne++
console.log(myNumberOne)

let myNumber1 = 10
console.log(++myNumber1)

let myNumberTwo = 10
myNumberTwo--
myNumberTwo--
console.log(myNumberTwo)

let myNumber2 = 10
console.log(--myNumber2)

console.log(2 ** 2)