/* 
    OPERADORES DE ATRIBUIÇÃO
        - Atribuição    ( = )
        - Adição        ( += )
        - Subtração     ( -= )
        - Divisão       ( /= )
        - Multiplicação ( *= )
        - Resto         ( %= )
*/

let firstNumber = 10 // ATRIBUIÇÃO

firstNumber += 2 // ADIÇÃO +2

firstNumber -= 2 // SUBTRAÇÃO -2

firstNumber /= 2 // DIVISÃO /2

firstNumber *= 2 // MULTIPLICAÇÃO *2

firstNumber %= 2 // RESTO %2
